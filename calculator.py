def add(a, b): 
    """add a and b (a+b) and returns the sum"""
    return a + b
def sub(a, b):
    """subtract b from a and returns it"""
    return a - b
def mult(a, b): 
    """multiply a and b and returns the result"""
    return a * b
def div(a, b):
    """divide a/b and returns the result"""
    return a / b
 


